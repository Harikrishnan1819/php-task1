<h2>Switch Case</h2>
<?php
$name = "sheik";
switch ($name) {
    case "hari":
        echo "Hari's birthday month is April..!";
        break;
    case "sheik":
        echo "Sheik's birthday month is October..!";
        break;
    case "joeal":
        echo "Joel's birthday month is August..!";
        break;
    case "balaji":
        echo "Balaji'sbirthday month is March..!";
        break;
    case "prashanth":
        echo "Prashanth's birthday month is September..!";
        break;
    case "kathir":
        echo "Kathir's birthday month is September..!";
        break;
    case "garu":
        echo "Garu's birthday month is June..!";
        break;
    default:
        echo "NONE";
}
?>